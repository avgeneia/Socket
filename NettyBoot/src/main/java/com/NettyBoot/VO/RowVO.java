package com.NettyBoot.VO;

public class RowVO {
	
	String id;
	int poz;
	int size;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getPoz() {
		return poz;
	}
	public void setPoz(int poz) {
		this.poz = poz;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
}
