package vo;

public class TelegramVO {
	
	String interfaceId;
	String id;
	int size;
	int poz;
	
	public int getPoz() {
		return poz;
	}
	public void setPoz(int poz) {
		this.poz = poz;
	}
	public String getInterfaceId() {
		return interfaceId;
	}
	public void setInterfaceId(String interfaceId) {
		this.interfaceId = interfaceId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
}
